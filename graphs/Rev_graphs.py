#Graphs data structure
#Graphs data structure consists of a collection of nodes or vertices connected by edges
'''
Graphs are used to represent relationships or connections between objects.
Undirected Graph: In an undirected graph, edges have no direction and the represent symmetric relationship
between nodes. If there is an edge between node A and node B, you can travel from A to B and from
B to A.
Directed Graph: In directed graph, edges have a direction, indicating a one-way relationship between
nodes. If there is an edge from node A to node B, you can travel from A to B but not necessarily from
B to A.

Weighted Graph: In a weighted graph, edges have associated weights or costs. 
These weights can represent various attributes such as distance, cost, or capacity.

Cyclic Graph: A cyclic graph contains at least one cycle, 
which is a path that starts and ends at the same node. In other words, 
you can traverse the graph and return to a previously visited node by following the edges.

Acyclic Graph: An acyclic graph, as the name suggests, does not contain any cycles. 
This type of graph is often used in scenarios where a cycle would be nonsensical or undesirable, 
such as representing dependencies between tasks or events.

Tree: A tree is a special type of acyclic graph where each node has a unique parent except for the 
root node, which has no parent. Trees have a hierarchical structure and are frequently used in 
data structures like binary trees or decision trees.
'''

